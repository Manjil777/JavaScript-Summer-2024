// Passing Value
// most used function in js : Arrow Function

let fun1 = (a, b, c) => {
  console.log(a, b, c);
};

fun1(1, 2, 3);

// it is not necessary to have same number of argument and parameter.

let fun2 = (a, b, c, d, e) => {
  // parameter
  console.log(a, b, c);
};

fun2(1, 2, 3); // Argument
