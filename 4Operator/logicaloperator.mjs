// Logical Operator

/*
& : ampersand
| : pipe

*/

/*
! : not
&& : and
|| : or
*/

// ! : not { O/I will be opposite if true : false and if false : true}
console.log(true);
console.log(!true);

// && : and { o/i will be true if all are true }

console.log(true && true);
console.log(true && false);

//  || : or {o/I will be true even if one is true}
console.log(true || true);
console.log(true || false);
console.log(false || false);
