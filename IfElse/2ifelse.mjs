/* 
  if gender is male =>  print he is male.
  if gender is female => print she is female.
  if gender is other => print "other"
  if gender is neither male nor female => print "Unknown Gender is Detected!!"
*/

let gender = "female";

if (gender === "male") {
  console.log("He is Male.");
} else if (gender === "female") {
  console.log("She is Female.");
} else if (gender === "other") {
  console.log("Other.");
} else {
  console.log("Unknown Gender is Detected!!!");
}
